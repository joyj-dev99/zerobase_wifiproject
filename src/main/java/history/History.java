package history;

public class History {
	
	private int historyId;
	private double historyLat;
	private double historyLnt;
	private String historyDt;
	
	public int getHistoryId() {
		return historyId;
	}
	public void setHistoryId(int historyId) {
		this.historyId = historyId;
	}
	public double getHistoryLat() {
		return historyLat;
	}
	public void setHistoryLat(double historyLat) {
		this.historyLat = historyLat;
	}
	public double getHistoryLnt() {
		return historyLnt;
	}
	public void setHistoryLnt(double historyLnt) {
		this.historyLnt = historyLnt;
	}
	public String getHistoryDt() {
		return historyDt;
	}
	public void setHistoryDt(String historyDt) {
		this.historyDt = historyDt;
	}

	
}
